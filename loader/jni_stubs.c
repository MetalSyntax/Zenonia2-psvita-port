#include <stdio.h>
#include <stdlib.h>
#include <string.h>

void* CallStaticObjectMethod_readAssets(const char* filename) {
    game_log("[FakeJNI] CallStaticObjectMethod -> readAssets: %s\n", filename);
    
    char path[256];
    snprintf(path, sizeof(path), "ux0:data/zenonia-2/%s", filename);
    
    FILE* f = fopen(path, "rb");
    if (!f) {
        game_log("[FakeJNI] readAssets: Failed to open %s\n", path);
        return (void*)0;
    }
    
    fseek(f, 0, SEEK_END);
    size_t size = ftell(f);
    fseek(f, 0, SEEK_SET);
    
    // Allocate Dalvik ArrayObject (16 bytes header + data)
    void* array_obj = malloc(16 + size);
    if (!array_obj) {
        fclose(f);
        return (void*)0;
    }
    
    memset(array_obj, 0, 16); // zero header
    fread((char*)array_obj + 16, 1, size, f);
    fclose(f);
    
    return array_obj;
}

